package operator;

public class UnaryOperators {
	public static void main(String[] args) {
		int a = 5;
		int b = 7;
		boolean c = false;  
		boolean d = true; 
		System.out.println("a++ = " + (a++));
		System.out.println("++a =  " + (++a));
		System.out.println("b-- = " + (b--));
		System.out.println("--b = " + (--b));
		System.out.println(!c);
		System.out.println(!d);
	}

}
