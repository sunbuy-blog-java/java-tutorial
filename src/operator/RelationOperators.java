package operator;

public class RelationOperators {
	public static void main(String[] args) {
		int a = 1;
		int b = 2;
		int c = 3;

		System.out.println("a > b: " + (a > b));
		System.out.println("a < b: " + (a < b));
		System.out.println("a >= c: " + (a >= b));
		System.out.println("a <= c: " + (a <= b));
		System.out.println("b == c: " + (a == c));
		System.out.println("b != c: " + (a != c));
	}
}
