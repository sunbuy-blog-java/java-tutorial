package operator;

public class LogicOperators {
	public static void main (String[] args) {
        boolean a = false;
        boolean b = true;
       
        System.out.println("a && b: " + (a && b));
        System.out.println("a || b: " + (a || b));
        System.out.println("!a: " + (!a));
        System.out.println("!b: " + (!b));
    }
}
