package operator;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int a = 100;
		int b = 15;

		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a % b = " + (a % b));

	}
}
